import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import AvatarGroup from './AvatarGroup.vue'

describe('AvatarGroup', () => {
  const avatars = [{ name: 'Alice Johnson' }, { name: 'Bob Smith' }, { name: 'Carol Davis' }]

  it('renders with data-rig-avatar-group', () => {
    const wrapper = mount(AvatarGroup, { props: { avatars } })
    expect(wrapper.attributes('data-rig-avatar-group')).toBe('')
  })

  it('renders all avatars when under max', () => {
    const wrapper = mount(AvatarGroup, { props: { avatars, max: 5 } })
    const items = wrapper.findAll('[data-rig-avatar-group-item]')
    expect(items).toHaveLength(3)
  })

  it('shows overflow when exceeding max', () => {
    const wrapper = mount(AvatarGroup, { props: { avatars, max: 2 } })
    const items = wrapper.findAll('[data-rig-avatar-group-item]')
    expect(items).toHaveLength(2)
    const overflow = wrapper.find('[data-rig-avatar-group-overflow]')
    expect(overflow.exists()).toBe(true)
    expect(overflow.text()).toBe('+1')
  })

  it('displays initials for avatars without src', () => {
    const wrapper = mount(AvatarGroup, { props: { avatars } })
    const initials = wrapper.findAll('[data-rig-avatar-group-initials]')
    expect(initials[0]!.text()).toBe('AJ')
  })

  it('renders img when src is provided', () => {
    const withSrc = [{ name: 'Test', src: '/test.jpg' }]
    const wrapper = mount(AvatarGroup, { props: { avatars: withSrc } })
    expect(wrapper.find('[data-rig-avatar-group-img]').exists()).toBe(true)
  })

  it('has group role with label', () => {
    const wrapper = mount(AvatarGroup, { props: { avatars } })
    expect(wrapper.attributes('role')).toBe('group')
    expect(wrapper.attributes('aria-label')).toBe('3 members')
  })

  it('supports all size variants', () => {
    for (const size of ['xs', 'sm', 'md', 'lg', 'xl'] as const) {
      const wrapper = mount(AvatarGroup, { props: { avatars, size } })
      expect(wrapper.attributes('data-size')).toBe(size)
    }
  })

  it('ignores keyboard events (non-interactive)', async () => {
    const wrapper = mount(AvatarGroup, { props: { avatars: [{ name: 'Jane' }] } })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.emitted('click')).toBeUndefined()
  })

  it('has correct ARIA role and label', () => {
    const wrapper = mount(AvatarGroup, { props: { avatars: [{ name: 'Jane' }, { name: 'John' }] } })
    expect(wrapper.attributes('role')).toBe('group')
    expect(wrapper.attributes('aria-label')).toContain('members')
  })

  it('shows overflow indicator when avatars exceed max', () => {
    const wrapper = mount(AvatarGroup, {
      props: { avatars: [{ name: 'A' }, { name: 'B' }, { name: 'C' }], max: 2 },
    })
    expect(wrapper.find('[data-rig-avatar-group-overflow]').exists()).toBe(true)
  })

  it('updates member count when avatars change', async () => {
    const wrapper = mount(AvatarGroup, { props: { avatars: [{ name: 'Jane' }] } })
    await wrapper.setProps({ avatars: [{ name: 'Jane' }, { name: 'John' }, { name: 'Doe' }] })
    expect(wrapper.attributes('aria-label')).toContain('3')
  })
})

