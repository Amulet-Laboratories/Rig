import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import PulseIndicator from './PulseIndicator.vue'

describe('PulseIndicator', () => {
  it('renders with data-rig-pulse-indicator', () => {
    const wrapper = mount(PulseIndicator)
    expect(wrapper.attributes('data-rig-pulse-indicator')).toBeDefined()
  })

  it('is active by default', () => {
    const wrapper = mount(PulseIndicator)
    expect(wrapper.attributes('data-active')).toBeDefined()
  })

  it('removes active attribute when inactive', () => {
    const wrapper = mount(PulseIndicator, { props: { active: false } })
    expect(wrapper.attributes('data-active')).toBeUndefined()
  })

  it('renders ring and dot sub-elements', () => {
    const wrapper = mount(PulseIndicator)
    expect(wrapper.find('[data-rig-pulse-indicator-ring]').exists()).toBe(true)
    expect(wrapper.find('[data-rig-pulse-indicator-dot]').exists()).toBe(true)
  })

  it('has role status', () => {
    const wrapper = mount(PulseIndicator)
    expect(wrapper.attributes('role')).toBe('status')
  })

  it('has accessible label', () => {
    const wrapper = mount(PulseIndicator, { props: { active: true } })
    expect(wrapper.attributes('aria-label')).toBe('Active')
  })

  it('applies custom color via style', () => {
    const wrapper = mount(PulseIndicator, { props: { color: '#00ff00' } })
    expect(wrapper.attributes('style')).toContain('--pulse-color')
  })

  it('ignores keyboard events (non-interactive)', async () => {
    const wrapper = mount(PulseIndicator, { props: { active: true } })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.emitted('click')).toBeUndefined()
  })

  it('has status role with Active label', () => {
    const wrapper = mount(PulseIndicator, { props: { active: true } })
    expect(wrapper.attributes('role')).toBe('status')
    expect(wrapper.attributes('aria-label')).toBe('Active')
  })

  it('renders dot and ring elements', () => {
    const wrapper = mount(PulseIndicator, { props: { active: true } })
    expect(wrapper.find('[data-rig-pulse-indicator-dot]').exists()).toBe(true)
    expect(wrapper.find('[data-rig-pulse-indicator-ring]').exists()).toBe(true)
  })

  it('updates aria-label when active changes to false', async () => {
    const wrapper = mount(PulseIndicator, { props: { active: true } })
    expect(wrapper.attributes('aria-label')).toBe('Active')
    await wrapper.setProps({ active: false })
    expect(wrapper.attributes('aria-label')).toBe('Inactive')
  })
})

