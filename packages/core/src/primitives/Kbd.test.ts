import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import Kbd from './Kbd.vue'

describe('Kbd', () => {
  it('renders with data-rig-kbd', () => {
    const wrapper = mount(Kbd)
    expect(wrapper.element.tagName).toBe('KBD')
    expect(wrapper.attributes('data-rig-kbd')).toBeDefined()
  })

  it('renders keys prop', () => {
    const wrapper = mount(Kbd, { props: { keys: 'Ctrl+S' } })
    expect(wrapper.text()).toBe('Ctrl+S')
  })

  it('renders slot content over keys prop', () => {
    const wrapper = mount(Kbd, {
      props: { keys: 'Ctrl+S' },
      slots: { default: '⌘S' },
    })
    expect(wrapper.text()).toBe('⌘S')
  })

  it('ignores keyboard events (non-interactive)', async () => {
    const wrapper = mount(Kbd)
    await wrapper.trigger('keydown', { key: 'Escape' })
    expect(wrapper.emitted('click')).toBeUndefined()
  })

  it('renders kbd element with data-rig-kbd', () => {
    const wrapper = mount(Kbd)
    expect(wrapper.find('[data-rig-kbd]').exists()).toBe(true)
  })

  it('displays key text content', () => {
    const wrapper = mount(Kbd, { props: { keys: 'Ctrl+S' } })
    expect(wrapper.text()).toContain('Ctrl')
  })

  it('updates displayed keys on prop change', async () => {
    const wrapper = mount(Kbd, { props: { keys: 'Ctrl+S' } })
    await wrapper.setProps({ keys: 'Ctrl+Z' })
    expect(wrapper.text()).toContain('Z')
  })
})

