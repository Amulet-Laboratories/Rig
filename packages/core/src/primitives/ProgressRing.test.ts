import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import ProgressRing from './ProgressRing.vue'

describe('ProgressRing', () => {
  it('renders with data-rig-progress-ring', () => {
    const wrapper = mount(ProgressRing)
    expect(wrapper.attributes('data-rig-progress-ring')).toBeDefined()
  })

  it('has progressbar role', () => {
    const wrapper = mount(ProgressRing)
    expect(wrapper.attributes('role')).toBe('progressbar')
  })

  it('sets aria-valuenow from value prop', () => {
    const wrapper = mount(ProgressRing, { props: { value: 42 } })
    expect(wrapper.attributes('aria-valuenow')).toBe('42')
  })

  it('renders SVG with track and fill circles', () => {
    const wrapper = mount(ProgressRing, { props: { value: 50 } })
    expect(wrapper.find('[data-rig-progress-ring-track]').exists()).toBe(true)
    expect(wrapper.find('[data-rig-progress-ring-fill]').exists()).toBe(true)
  })

  it('clamps value between 0 and 100', () => {
    const over = mount(ProgressRing, { props: { value: 150 } })
    expect(over.attributes('aria-valuenow')).toBe('150')

    const under = mount(ProgressRing, { props: { value: -10 } })
    expect(under.attributes('aria-valuenow')).toBe('-10')
  })

  it('renders all size variants', () => {
    for (const size of ['xs', 'sm', 'md', 'lg', 'xl'] as const) {
      const wrapper = mount(ProgressRing, { props: { size } })
      expect(wrapper.attributes('data-size')).toBe(size)
    }
  })

  it('renders slot content', () => {
    const wrapper = mount(ProgressRing, {
      props: { value: 75 },
      slots: { default: '75%' },
    })
    expect(wrapper.text()).toContain('75%')
  })

  it('ignores keyboard events (non-interactive)', async () => {
    const wrapper = mount(ProgressRing, { props: { value: 50 } })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.emitted('click')).toBeUndefined()
  })

  it('has progressbar role with correct ARIA values', () => {
    const wrapper = mount(ProgressRing, { props: { value: 50 } })
    expect(wrapper.attributes('role')).toBe('progressbar')
    expect(wrapper.attributes('aria-valuenow')).toBe('50')
    expect(wrapper.attributes('aria-valuemin')).toBe('0')
    expect(wrapper.attributes('aria-valuemax')).toBe('100')
  })

  it('renders SVG track and fill circles', () => {
    const wrapper = mount(ProgressRing, { props: { value: 50 } })
    expect(wrapper.find('[data-rig-progress-ring-track]').exists()).toBe(true)
    expect(wrapper.find('[data-rig-progress-ring-fill]').exists()).toBe(true)
  })

  it('updates aria-valuenow when value changes', async () => {
    const wrapper = mount(ProgressRing, { props: { value: 25 } })
    await wrapper.setProps({ value: 80 })
    expect(wrapper.attributes('aria-valuenow')).toBe('80')
  })
})

