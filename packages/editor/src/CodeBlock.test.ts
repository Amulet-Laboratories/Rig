import { describe, it, expect, vi } from 'vitest'
import { mount } from '@vue/test-utils'
import CodeBlock from './CodeBlock.vue'

describe('CodeBlock', () => {
  it('renders with data-rig-code-block', () => {
    const wrapper = mount(CodeBlock, { props: { code: 'hello' } })
    expect(wrapper.attributes('data-rig-code-block')).toBeDefined()
  })

  it('displays code content', () => {
    const wrapper = mount(CodeBlock, { props: { code: 'const x = 1' } })
    expect(wrapper.text()).toContain('const x = 1')
  })

  it('shows line numbers by default', () => {
    const wrapper = mount(CodeBlock, { props: { code: 'line1\nline2' } })
    expect(wrapper.findAll('[data-rig-code-block-line-number]')).toHaveLength(2)
  })

  it('hides line numbers when disabled', () => {
    const wrapper = mount(CodeBlock, { props: { code: 'line1', showLineNumbers: false } })
    expect(wrapper.find('[data-rig-code-block-line-number]').exists()).toBe(false)
  })

  it('shows language in header', () => {
    const wrapper = mount(CodeBlock, { props: { code: 'x', language: 'typescript' } })
    expect(wrapper.find('[data-rig-code-block-language]').text()).toBe('typescript')
  })

  it('shows copy button by default', () => {
    const wrapper = mount(CodeBlock, { props: { code: 'x' } })
    expect(wrapper.find('[data-rig-code-block-copy]').exists()).toBe(true)
  })

  it('hides copy button when disabled', () => {
    const wrapper = mount(CodeBlock, { props: { code: 'x', copyable: false } })
    expect(wrapper.find('[data-rig-code-block-copy]').exists()).toBe(false)
  })

  it('stops keyboard event propagation', async () => {
    const wrapper = mount(CodeBlock, { props: { code: 'hello' } })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.text()).toContain('hello')
    expect(wrapper.find('[data-rig-code-block-code]').exists()).toBe(true)
  })

  it('supports focus management', async () => {
    const wrapper = mount(CodeBlock, { props: { code: 'hello' }, attachTo: document.body })
    const el = wrapper.element as HTMLElement
    el.focus()
    expect(document.activeElement).toBe(el)
    wrapper.unmount()
  })

  it('copy button has correct aria-label', () => {
    const wrapper = mount(CodeBlock, { props: { code: 'hello' } })
    const copyBtn = wrapper.find('[data-rig-code-block-copy]')
    expect(copyBtn.attributes('aria-label')).toBe('Copy code')
    expect(copyBtn.text()).toBe('Copy')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(CodeBlock, { props: { code: 'hello' } })
    await wrapper.setProps({ code: 'world' })
    expect(wrapper.text()).toContain('world')
    expect(wrapper.text()).not.toContain('hello')
  })
})

