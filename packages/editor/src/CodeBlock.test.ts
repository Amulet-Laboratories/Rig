describe('CodeBlock interactions', () => {
  it('can emit events', async () => {
    const wrapper = mount(CodeBlock, {
      attachTo: document.body,
    })
    const clickable = wrapper.find('button, [role="button"], [tabindex], a')
    if (clickable.exists()) {
      await clickable.trigger('click')
    } else {
      await wrapper.trigger('click')
    }
    const events = wrapper.emitted()
    expect(events).toBeDefined()
    wrapper.unmount()
  })

  it('copies code to clipboard and shows copied state', async () => {
    const writeTextMock = vi.fn().mockResolvedValue(undefined)
    Object.assign(navigator, {
      clipboard: { writeText: writeTextMock },
    })
    const wrapper = mount(CodeBlock, {
      props: { code: 'const x = 1' },
      attachTo: document.body,
    })
    const copyBtn = wrapper.find('[data-rig-code-block-copy]')
    await copyBtn.trigger('click')
    await wrapper.vm.$nextTick()
    expect(writeTextMock).toHaveBeenCalledWith('const x = 1')
    expect(copyBtn.text()).toBe('Copied')
    expect(copyBtn.attributes('aria-label')).toBe('Copied')
    expect(copyBtn.attributes('data-state')).toBe('copied')
    wrapper.unmount()
  })

  it('resets copied state after timeout', async () => {
    vi.useFakeTimers()
    const writeTextMock = vi.fn().mockResolvedValue(undefined)
    Object.assign(navigator, {
      clipboard: { writeText: writeTextMock },
    })
    const wrapper = mount(CodeBlock, {
      props: { code: 'hello' },
      attachTo: document.body,
    })
    const copyBtn = wrapper.find('[data-rig-code-block-copy]')
    await copyBtn.trigger('click')
    await wrapper.vm.$nextTick()
    expect(copyBtn.text()).toBe('Copied')
    vi.advanceTimersByTime(2000)
    await wrapper.vm.$nextTick()
    expect(copyBtn.text()).toBe('Copy')
    expect(copyBtn.attributes('data-state')).toBeUndefined()
    vi.useRealTimers()
    wrapper.unmount()
  })

  it('handles clipboard API failure gracefully', async () => {
    Object.assign(navigator, {
      clipboard: {
        writeText: vi.fn().mockRejectedValue(new Error('Denied')),
      },
    })
    const wrapper = mount(CodeBlock, {
      props: { code: 'test' },
      attachTo: document.body,
    })
    const copyBtn = wrapper.find('[data-rig-code-block-copy]')
    await copyBtn.trigger('click')
    await wrapper.vm.$nextTick()
    // Should not crash, copied stays false
    expect(copyBtn.text()).toBe('Copy')
    wrapper.unmount()
  })

  it('sets data-language attribute', () => {
    const wrapper = mount(CodeBlock, {
      props: { code: 'x', language: 'python' },
    })
    expect(wrapper.attributes('data-language')).toBe('python')
  })

  it('has tabindex 0 for focus', () => {
    const wrapper = mount(CodeBlock, { props: { code: 'x' } })
    expect(wrapper.attributes('tabindex')).toBe('0')
  })
})

