import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import DiffViewer from './DiffViewer.vue'

describe('DiffViewer', () => {
  it('renders with data-rig-diff-viewer', () => {
    const wrapper = mount(DiffViewer)
    expect(wrapper.attributes('data-rig-diff-viewer')).toBeDefined()
  })

  it('defaults to unified mode', () => {
    const wrapper = mount(DiffViewer)
    expect(wrapper.attributes('data-mode')).toBe('unified')
  })

  it('supports split mode', () => {
    const wrapper = mount(DiffViewer, { props: { mode: 'split' } })
    expect(wrapper.attributes('data-mode')).toBe('split')
  })

  it('shows added and removed lines', () => {
    const wrapper = mount(DiffViewer, {
      props: { original: 'hello', modified: 'world' },
    })
    expect(wrapper.find('[data-type="removed"]').exists()).toBe(true)
    expect(wrapper.find('[data-type="added"]').exists()).toBe(true)
  })

  it('shows stats in header', () => {
    const wrapper = mount(DiffViewer, {
      props: { original: 'a\nb', modified: 'a\nc' },
    })
    expect(wrapper.find('[data-rig-diff-viewer-added]').exists()).toBe(true)
    expect(wrapper.find('[data-rig-diff-viewer-removed]').exists()).toBe(true)
  })

  it('has region role with label', () => {
    const wrapper = mount(DiffViewer)
    expect(wrapper.attributes('role')).toBe('region')
    expect(wrapper.attributes('aria-label')).toBe('Code diff')
  })

  it('stops keyboard event propagation', async () => {
    const wrapper = mount(DiffViewer, { props: { original: 'a', modified: 'b' } })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.find('[data-type="removed"]').exists()).toBe(true)
    expect(wrapper.find('[data-type="added"]').exists()).toBe(true)
  })

  it('supports focus management', async () => {
    const wrapper = mount(DiffViewer, {
      props: { original: 'a', modified: 'b' },
      attachTo: document.body,
    })
    const el = wrapper.element as HTMLElement
    el.focus()
    expect(document.activeElement).toBe(el)
    wrapper.unmount()
  })

  it('displays language attribute', () => {
    const wrapper = mount(DiffViewer, {
      props: { original: 'a', modified: 'b', language: 'typescript' },
    })
    expect(wrapper.attributes('data-language')).toBe('typescript')
    expect(wrapper.find('[data-rig-diff-viewer-language]').text()).toBe('typescript')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(DiffViewer, { props: { original: 'a', modified: 'b' } })
    await wrapper.setProps({ mode: 'split' })
    expect(wrapper.attributes('data-mode')).toBe('split')
    expect(wrapper.find('[data-rig-diff-viewer-split]').exists()).toBe(true)
  })
})

