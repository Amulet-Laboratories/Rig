import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import PlaybackControls from './PlaybackControls.vue'

describe('PlaybackControls', () => {
  it('renders with data-rig-playback-controls', () => {
    const wrapper = mount(PlaybackControls)
    expect(wrapper.find('[data-rig-playback-controls]').exists()).toBe(true)
  })

  it('renders play button', () => {
    const wrapper = mount(PlaybackControls)
    expect(wrapper.find('[data-rig-playback-controls-play]').exists()).toBe(true)
  })

  it('renders skip buttons', () => {
    const wrapper = mount(PlaybackControls)
    expect(wrapper.find('[data-rig-playback-controls-skip-back]').exists()).toBe(true)
    expect(wrapper.find('[data-rig-playback-controls-skip-forward]').exists()).toBe(true)
  })

  it('renders progress track', () => {
    const wrapper = mount(PlaybackControls)
    expect(wrapper.find('[data-rig-playback-controls-track]').exists()).toBe(true)
    expect(wrapper.find('[data-rig-playback-controls-progress]').exists()).toBe(true)
  })

  it('displays formatted time', () => {
    const wrapper = mount(PlaybackControls, { props: { currentTime: 65, duration: 300 } })
    expect(wrapper.find('[data-rig-playback-controls-current]').text()).toBe('1:05')
    expect(wrapper.find('[data-rig-playback-controls-duration]').text()).toBe('5:00')
  })

  it('emits play on button click when not playing', async () => {
    const wrapper = mount(PlaybackControls, { props: { playing: false } })
    await wrapper.find('[data-rig-playback-controls-play]').trigger('click')
    expect(wrapper.emitted('play')).toBeTruthy()
  })

  it('emits pause on button click when playing', async () => {
    const wrapper = mount(PlaybackControls, { props: { playing: true } })
    await wrapper.find('[data-rig-playback-controls-play]').trigger('click')
    expect(wrapper.emitted('pause')).toBeTruthy()
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(PlaybackControls)
    const el = wrapper.find('[data-rig-playback-controls]')
    expect(el.attributes('role')).toBe('group')
    expect(el.attributes('aria-label')).toBe('Playback controls')
  })

  it('shows speed control when enabled', () => {
    const wrapper = mount(PlaybackControls, { props: { showSpeed: true } })
    expect(wrapper.find('[data-rig-playback-controls-speed]').exists()).toBe(true)
  })

  it('hides speed control by default', () => {
    const wrapper = mount(PlaybackControls)
    expect(wrapper.find('[data-rig-playback-controls-speed]').exists()).toBe(false)
  })

  it('handles keyboard navigation', () => {
    const wrapper = mount(PlaybackControls)
    expect(wrapper.find('[data-rig-playback-controls-skip-back]').attributes('aria-label')).toBe(
      'Skip back 10 seconds',
    )
    expect(wrapper.find('[data-rig-playback-controls-skip-forward]').attributes('aria-label')).toBe(
      'Skip forward 10 seconds',
    )
  })

  it('supports focus management', () => {
    const wrapper = mount(PlaybackControls)
    expect(wrapper.find('[data-rig-playback-controls]').attributes('tabindex')).toBe('0')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(PlaybackControls, { props: { playing: false } })
    expect(wrapper.find('[data-rig-playback-controls-play]').attributes('aria-label')).toBe('Play')
    expect(wrapper.find('[data-rig-playback-controls-play]').text()).toBe('▶')
    await wrapper.setProps({ playing: true })
    expect(wrapper.find('[data-rig-playback-controls-play]').attributes('aria-label')).toBe('Pause')
    expect(wrapper.find('[data-rig-playback-controls-play]').text()).toBe('⏸')
  })

  it('emits seek on skip-back click', async () => {
    const wrapper = mount(PlaybackControls, { props: { currentTime: 30, duration: 100 } })
    await wrapper.find('[data-rig-playback-controls-skip-back]').trigger('click')
    expect(wrapper.emitted('seek')?.[0]?.[0]).toBe(20)
  })

  it('emits seek on skip-forward click', async () => {
    const wrapper = mount(PlaybackControls, { props: { currentTime: 30, duration: 100 } })
    await wrapper.find('[data-rig-playback-controls-skip-forward]').trigger('click')
    expect(wrapper.emitted('seek')?.[0]?.[0]).toBe(40)
  })

  it('cycles speed and emits speed-change', async () => {
    const wrapper = mount(PlaybackControls, { props: { showSpeed: true, speed: 1 } })
    const speedBtn = wrapper.find('[data-rig-playback-controls-speed]')
    await speedBtn.trigger('click')
    expect(wrapper.emitted('speed-change')?.[0]?.[0]).toBe(1.5)
  })
})

