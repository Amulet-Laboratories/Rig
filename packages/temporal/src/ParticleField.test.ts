import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import ParticleField from './ParticleField.vue'

describe('ParticleField', () => {
  it('renders with data-rig-particle-field', () => {
    const wrapper = mount(ParticleField, { props: { active: false } })
    expect(wrapper.find('[data-rig-particle-field]').exists()).toBe(true)
  })

  it('renders canvas element', () => {
    const wrapper = mount(ParticleField, { props: { active: false } })
    expect(wrapper.find('[data-rig-particle-field-canvas]').exists()).toBe(true)
  })

  it('applies dimensions', () => {
    const wrapper = mount(ParticleField, { props: { width: 600, height: 300, active: false } })
    const el = wrapper.find('[data-rig-particle-field]')
    expect(el.attributes('style')).toContain('width: 600px')
    expect(el.attributes('style')).toContain('height: 300px')
  })

  it('is hidden from assistive technology', () => {
    const wrapper = mount(ParticleField, { props: { active: false } })
    const el = wrapper.find('[data-rig-particle-field]')
    expect(el.attributes('role')).toBe('presentation')
    expect(el.attributes('aria-hidden')).toBe('true')
  })

  it('sets data-active when active', () => {
    const wrapper = mount(ParticleField, { props: { active: true } })
    expect(wrapper.find('[data-rig-particle-field]').attributes('data-active')).toBeDefined()
  })

  it('does not set data-active when inactive', () => {
    const wrapper = mount(ParticleField, { props: { active: false } })
    expect(wrapper.find('[data-rig-particle-field]').attributes('data-active')).toBeUndefined()
  })

  it('sets canvas dimensions', () => {
    const wrapper = mount(ParticleField, { props: { width: 400, height: 200, active: false } })
    const canvas = wrapper.find('canvas')
    expect(canvas.attributes('width')).toBe('400')
    expect(canvas.attributes('height')).toBe('200')
  })

  it('handles keyboard navigation', () => {
    const wrapper = mount(ParticleField, { props: { active: false } })
    expect(wrapper.find('[data-rig-particle-field]').attributes('tabindex')).toBe('-1')
  })

  it('supports focus management', () => {
    const wrapper = mount(ParticleField, { props: { active: false }, attachTo: document.body })
    const el = wrapper.find('[data-rig-particle-field]').element as HTMLElement
    el.focus()
    // tabindex=-1 allows programmatic focus but not tab focus
    expect(document.activeElement).toBe(el)
    wrapper.unmount()
  })

  it('handles emit interactions', () => {
    const wrapper = mount(ParticleField, { props: { active: false } })
    // Decorative component has no interactive children
    expect(wrapper.findAll('button')).toHaveLength(0)
    expect(wrapper.findAll('[role="button"]')).toHaveLength(0)
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(ParticleField, { props: { width: 400, height: 200, active: false } })
    expect(wrapper.find('canvas').attributes('width')).toBe('400')
    await wrapper.setProps({ width: 800 })
    expect(wrapper.find('canvas').attributes('width')).toBe('800')
    expect(wrapper.find('[data-rig-particle-field]').attributes('style')).toContain('width: 800px')
  })

  it('applies default dimensions', () => {
    const wrapper = mount(ParticleField, { props: { active: false } })
    const style = wrapper.find('[data-rig-particle-field]').attributes('style')
    expect(style).toContain('width: 500px')
    expect(style).toContain('height: 400px')
  })

  it('updates height on canvas and container', async () => {
    const wrapper = mount(ParticleField, { props: { width: 300, height: 200, active: false } })
    expect(wrapper.find('canvas').attributes('height')).toBe('200')
    await wrapper.setProps({ height: 500 })
    expect(wrapper.find('canvas').attributes('height')).toBe('500')
    expect(wrapper.find('[data-rig-particle-field]').attributes('style')).toContain('height: 500px')
  })

  it('renders without error with zero count', () => {
    const wrapper = mount(ParticleField, { props: { count: 0, active: false } })
    expect(wrapper.find('[data-rig-particle-field]').exists()).toBe(true)
  })

  it('renders without error with zero speed', () => {
    const wrapper = mount(ParticleField, { props: { speed: 0, active: false } })
    expect(wrapper.find('[data-rig-particle-field]').exists()).toBe(true)
  })

  it('renders with continuous false', () => {
    const wrapper = mount(ParticleField, { props: { continuous: false, active: false } })
    expect(wrapper.find('[data-rig-particle-field]').exists()).toBe(true)
  })
})

