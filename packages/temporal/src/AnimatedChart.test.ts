import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import AnimatedChart from './AnimatedChart.vue'

const frames = [
  { label: 'Jan', data: [10, 20, 30] },
  { label: 'Feb', data: [15, 25, 35] },
  { label: 'Mar', data: [20, 30, 40] },
]

describe('AnimatedChart', () => {
  it('renders with data-rig-animated-chart', () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    expect(wrapper.find('[data-rig-animated-chart]').exists()).toBe(true)
  })

  it('renders canvas SVG', () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    expect(wrapper.find('[data-rig-animated-chart-canvas]').exists()).toBe(true)
  })

  it('renders bars for bar type', () => {
    const wrapper = mount(AnimatedChart, { props: { frames, type: 'bar' } })
    expect(wrapper.findAll('[data-rig-animated-chart-bar]')).toHaveLength(3)
  })

  it('renders line for line type', () => {
    const wrapper = mount(AnimatedChart, { props: { frames, type: 'line' } })
    expect(wrapper.find('[data-rig-animated-chart-line]').exists()).toBe(true)
  })

  it('renders play button', () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    expect(wrapper.find('[data-rig-animated-chart-play]').exists()).toBe(true)
  })

  it('displays frame label', () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    expect(wrapper.find('[data-rig-animated-chart-label]').text()).toBe('Jan')
  })

  it('displays frame counter', () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    expect(wrapper.find('[data-rig-animated-chart-counter]').text()).toBe('1 / 3')
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    expect(wrapper.find('[data-rig-animated-chart]').attributes('role')).toBe('figure')
  })

  it('handles keyboard navigation', () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    expect(wrapper.find('[data-rig-animated-chart]').attributes('aria-label')).toBe(
      'Animated chart',
    )
  })

  it('supports focus management', () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    expect(wrapper.find('[data-rig-animated-chart]').attributes('tabindex')).toBe('0')
  })

  it('handles emit interactions', async () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    const playBtn = wrapper.find('[data-rig-animated-chart-play]')
    expect(playBtn.text()).toBe('▶')
    await playBtn.trigger('click')
    expect(playBtn.text()).toBe('⏸')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    expect(wrapper.find('[data-rig-animated-chart-counter]').text()).toBe('1 / 3')
    await wrapper.setProps({ frames: [{ label: 'Only', data: [5] }] })
    expect(wrapper.find('[data-rig-animated-chart-counter]').text()).toBe('1 / 1')
    expect(wrapper.find('[data-rig-animated-chart-label]').text()).toBe('Only')
  })

  it('applies custom width and height to svg', () => {
    const wrapper = mount(AnimatedChart, {
      props: { frames, width: 600, height: 400 },
    })
    const svg = wrapper.find('[data-rig-animated-chart-canvas]')
    expect(svg.attributes('width')).toBe('600')
    expect(svg.attributes('height')).toBe('400')
  })

  it('pauses after playing', async () => {
    const wrapper = mount(AnimatedChart, { props: { frames } })
    const playBtn = wrapper.find('[data-rig-animated-chart-play]')
    await playBtn.trigger('click')
    expect(playBtn.text()).toBe('\u23f8')
    await playBtn.trigger('click')
    expect(playBtn.text()).toBe('\u25b6')
  })

  it('renders empty state gracefully', () => {
    const wrapper = mount(AnimatedChart, { props: { frames: [] } })
    expect(wrapper.findAll('[data-rig-animated-chart-bar]')).toHaveLength(0)
    expect(wrapper.find('[data-rig-animated-chart-counter]').text()).toBe('1 / 0')
  })

  it('sets data-type attribute', () => {
    const wrapper = mount(AnimatedChart, { props: { frames, type: 'line' } })
    expect(wrapper.find('[data-rig-animated-chart]').attributes('data-type')).toBe('line')
  })

  it('renders line path for line type', () => {
    const wrapper = mount(AnimatedChart, { props: { frames, type: 'line' } })
    expect(wrapper.find('[data-rig-animated-chart-line]').exists()).toBe(true)
    expect(wrapper.findAll('[data-rig-animated-chart-bar]')).toHaveLength(0)
  })
})

