import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import DataGrid from './DataGrid.vue'

describe('DataGrid', () => {
  const columns = [
    { key: 'name', label: 'Name', sortable: true },
    { key: 'type', label: 'Type', sortable: true },
  ]
  const rows = [
    { name: 'Button', type: 'component' },
    { name: 'Avatar', type: 'component' },
  ]

  it('renders with data-rig-data-grid', () => {
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    expect(wrapper.attributes('data-rig-data-grid')).toBeDefined()
  })

  it('renders header cells', () => {
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    const headers = wrapper.findAll('[data-rig-data-grid-header-cell]')
    expect(headers).toHaveLength(2)
    expect(headers[0]!.text()).toContain('Name')
  })

  it('renders all rows', () => {
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    const rowEls = wrapper.findAll('[data-rig-data-grid-row]')
    expect(rowEls).toHaveLength(2)
  })

  it('sorts on header click', async () => {
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    const nameHeader = wrapper.findAll('[data-rig-data-grid-header-cell]')[0]!
    await nameHeader.trigger('click')
    const rowEls = wrapper.findAll('[data-rig-data-grid-row]')
    const firstCell = rowEls[0]!.findAll('[data-rig-data-grid-cell]')[0]!
    expect(firstCell.text()).toContain('Avatar')
  })

  it('has grid role', () => {
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    expect(wrapper.attributes('role')).toBe('grid')
  })

  it('emits row-click', async () => {
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.findAll('[data-rig-data-grid-row]')[0]!.trigger('click')
    expect(wrapper.emitted('row-click')).toBeTruthy()
  })

  it('handles keyboard navigation', async () => {
    const wrapper = mount(DataGrid, {
      props: {
        columns: [{ key: 'a', label: 'A' }],
        rows: [{ a: 1 }, { a: 2 }],
      },
    })
    const rows = wrapper.findAll('[data-rig-data-grid-row]')
    expect(rows[0]!.attributes('tabindex')).toBe('0')
    expect(rows[1]!.attributes('tabindex')).toBe('-1')
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    const updatedRows = wrapper.findAll('[data-rig-data-grid-row]')
    expect(updatedRows[0]!.attributes('tabindex')).toBe('-1')
    expect(updatedRows[1]!.attributes('tabindex')).toBe('0')
  })

  it('supports focus management', () => {
    const wrapper = mount(DataGrid, {
      props: {
        columns: [
          { key: 'a', label: 'A' },
          { key: 'b', label: 'B' },
        ],
        rows: [{ a: 1, b: 2 }],
      },
    })
    expect(wrapper.attributes('aria-rowcount')).toBe('2')
    expect(wrapper.attributes('aria-colcount')).toBe('2')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(DataGrid, {
      props: { columns: [{ key: 'a', label: 'A' }], rows: [{ a: 1 }] },
    })
    expect(wrapper.findAll('[data-rig-data-grid-row]')).toHaveLength(1)
    await wrapper.setProps({ rows: [] })
    expect(wrapper.findAll('[data-rig-data-grid-row]')).toHaveLength(0)
    expect(wrapper.find('[data-rig-data-grid-empty]').exists()).toBe(true)
  })

  it('toggles sort direction on repeated header click', async () => {
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    const nameHeader = wrapper.findAll('[data-rig-data-grid-header-cell]')[0]!
    await nameHeader.trigger('click')
    expect(nameHeader.attributes('aria-sort')).toBe('ascending')
    await nameHeader.trigger('click')
    expect(nameHeader.attributes('aria-sort')).toBe('descending')
  })

  it('renders empty slot when no rows', () => {
    const wrapper = mount(DataGrid, {
      props: { columns, rows: [] },
      slots: { empty: '<p>Nothing here</p>' },
    })
    expect(wrapper.find('[data-rig-data-grid-empty]').text()).toBe('Nothing here')
  })
})

