describe('DataGrid interactions', () => {
  it('supports focus management', async () => {
    const wrapper = mount(DataGrid, {
      props: { columns: [{ key: 'name', label: 'Name' }], rows: [{ name: 'Test' }] }, attachTo: document.body,
    })
    const focusable = wrapper.find('[tabindex], input, button, a')
    if (focusable.exists()) {
      ;(focusable.element as HTMLElement).focus()
      expect(document.activeElement).toBe(focusable.element)
    } else {
      // Grid component — verify role is set for screen readers
      expect(wrapper.find('[role]').exists()).toBe(true)
    }
    wrapper.unmount()
  })

  it('emits sort event on sortable header click', async () => {
    const columns = [
      { key: 'name', label: 'Name', sortable: true },
      { key: 'type', label: 'Type' },
    ]
    const rows = [{ name: 'B', type: 'x' }, { name: 'A', type: 'y' }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.findAll('[data-rig-data-grid-header-cell]')[0]!.trigger('click')
    // data-sorted should be set
    expect(
      wrapper.findAll('[data-rig-data-grid-header-cell]')[0]!.attributes('data-sorted'),
    ).toBe('asc')
    wrapper.unmount()
  })

  it('does not sort on non-sortable header click', async () => {
    const columns = [{ key: 'name', label: 'Name' }]
    const rows = [{ name: 'B' }, { name: 'A' }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.findAll('[data-rig-data-grid-header-cell]')[0]!.trigger('click')
    expect(
      wrapper.findAll('[data-rig-data-grid-header-cell]')[0]!.attributes('data-sorted'),
    ).toBeUndefined()
    wrapper.unmount()
  })

  it('Enter key on sortable header toggles sort', async () => {
    const columns = [{ key: 'name', label: 'Name', sortable: true }]
    const rows = [{ name: 'B' }, { name: 'A' }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.findAll('[data-rig-data-grid-header-cell]')[0]!.trigger('keydown.enter')
    expect(
      wrapper.findAll('[data-rig-data-grid-header-cell]')[0]!.attributes('aria-sort'),
    ).toBe('ascending')
    wrapper.unmount()
  })

  it('Space key on sortable header toggles sort', async () => {
    const columns = [{ key: 'name', label: 'Name', sortable: true }]
    const rows = [{ name: 'B' }, { name: 'A' }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.findAll('[data-rig-data-grid-header-cell]')[0]!.trigger('keydown.space')
    expect(
      wrapper.findAll('[data-rig-data-grid-header-cell]')[0]!.attributes('aria-sort'),
    ).toBe('ascending')
    wrapper.unmount()
  })

  it('ArrowRight moves focus to next column', async () => {
    const columns = [
      { key: 'a', label: 'A' },
      { key: 'b', label: 'B' },
    ]
    const rows = [{ a: 1, b: 2 }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.trigger('keydown', { key: 'ArrowRight' })
    // focusedCol should now be 1
    const cells = wrapper.findAll('[data-rig-data-grid-cell]')
    // After ArrowRight, focusedCol=1 — we can verify the grid updated internally
    expect(cells.length).toBe(2)
    wrapper.unmount()
  })

  it('ArrowLeft does not go below column 0', async () => {
    const columns = [{ key: 'a', label: 'A' }]
    const rows = [{ a: 1 }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.trigger('keydown', { key: 'ArrowLeft' })
    // Should not crash, focusedCol stays 0
    expect(wrapper.html()).toBeTruthy()
    wrapper.unmount()
  })

  it('Home key moves focus to first column', async () => {
    const columns = [
      { key: 'a', label: 'A' },
      { key: 'b', label: 'B' },
    ]
    const rows = [{ a: 1, b: 2 }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.trigger('keydown', { key: 'ArrowRight' })
    await wrapper.trigger('keydown', { key: 'Home' })
    // Should not crash
    expect(wrapper.html()).toBeTruthy()
    wrapper.unmount()
  })

  it('End key moves focus to last column', async () => {
    const columns = [
      { key: 'a', label: 'A' },
      { key: 'b', label: 'B' },
    ]
    const rows = [{ a: 1, b: 2 }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.trigger('keydown', { key: 'End' })
    expect(wrapper.html()).toBeTruthy()
    wrapper.unmount()
  })

  it('Enter key emits row-click for focused row', async () => {
    const columns = [{ key: 'a', label: 'A' }]
    const rows = [{ a: 1 }, { a: 2 }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.trigger('keydown', { key: 'Enter' })
    expect(wrapper.emitted('row-click')?.[0]).toEqual([{ a: 1 }, 0])
    wrapper.unmount()
  })

  it('Space key emits row-click for focused row', async () => {
    const columns = [{ key: 'a', label: 'A' }]
    const rows = [{ a: 1 }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.trigger('keydown', { key: ' ' })
    expect(wrapper.emitted('row-click')?.[0]).toEqual([{ a: 1 }, 0])
    wrapper.unmount()
  })

  it('ArrowUp does not go below row 0', async () => {
    const columns = [{ key: 'a', label: 'A' }]
    const rows = [{ a: 1 }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    await wrapper.trigger('keydown', { key: 'ArrowUp' })
    expect(wrapper.html()).toBeTruthy()
    wrapper.unmount()
  })

  it('shows sort indicator on sortable columns', () => {
    const columns = [
      { key: 'name', label: 'Name', sortable: true },
      { key: 'type', label: 'Type' },
    ]
    const wrapper = mount(DataGrid, { props: { columns, rows: [] } })
    expect(wrapper.find('[data-rig-data-grid-sort-indicator]').exists()).toBe(true)
    wrapper.unmount()
  })

  it('sets column width style when specified', () => {
    const columns = [{ key: 'name', label: 'Name', width: 200 }]
    const rows = [{ name: 'Test' }]
    const wrapper = mount(DataGrid, { props: { columns, rows } })
    const header = wrapper.find('[data-rig-data-grid-header-cell]')
    expect(header.attributes('style')).toContain('width: 200px')
    wrapper.unmount()
  })

  it('renders default "No data" when empty and no empty slot', () => {
    const columns = [{ key: 'name', label: 'Name' }]
    const wrapper = mount(DataGrid, { props: { columns, rows: [] } })
    expect(wrapper.find('[data-rig-data-grid-empty]').text()).toBe('No data')
    wrapper.unmount()
  })
})

