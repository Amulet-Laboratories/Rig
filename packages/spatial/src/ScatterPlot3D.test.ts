import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import ScatterPlot3D from './ScatterPlot3D.vue'

const data = [
  { x: 1, y: 2, z: 3 },
  { x: 4, y: 5, z: 6, label: 'A', color: '#ff0000' },
]

describe('ScatterPlot3D', () => {
  it('renders with data-rig-scatter-plot-3d', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    expect(wrapper.find('[data-rig-scatter-plot-3d]').exists()).toBe(true)
  })

  it('renders points', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    expect(wrapper.findAll('[data-rig-scatter-plot-3d-point]')).toHaveLength(2)
  })

  it('renders axes', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    expect(wrapper.findAll('[data-rig-scatter-plot-3d-axis]')).toHaveLength(3)
  })

  it('renders axis labels', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    const labels = wrapper.findAll('[data-rig-scatter-plot-3d-axis-label]')
    expect(labels).toHaveLength(3)
  })

  it('emits point-click on click', async () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    await wrapper.findAll('[data-rig-scatter-plot-3d-point]')[0]!.trigger('click')
    expect(wrapper.emitted('point-click')).toBeTruthy()
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    expect(wrapper.find('svg').attributes('role')).toBe('img')
  })

  it('handles keyboard navigation', async () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    await wrapper.find('svg').trigger('keydown', { key: 'Enter' })
    expect(wrapper.emitted('point-click')).toBeUndefined()
  })

  it('supports focus management', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    expect(wrapper.find('svg').attributes('tabindex')).toBe('0')
    expect(wrapper.find('svg').attributes('aria-label')).toBe('3D scatter plot')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    expect(wrapper.findAll('[data-rig-scatter-plot-3d-point]')).toHaveLength(2)
    await wrapper.setProps({ data: [...data, { x: 7, y: 8, z: 9 }] })
    expect(wrapper.findAll('[data-rig-scatter-plot-3d-point]')).toHaveLength(3)
  })

  it('emits point-click with correct payload', async () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    await wrapper.findAll('[data-rig-scatter-plot-3d-point]')[1]!.trigger('click')
    const emitted = wrapper.emitted('point-click')?.[0]
    expect(emitted?.[0]).toMatchObject({ x: 4, y: 5, z: 6, label: 'A' })
    expect(emitted?.[1]).toBe(1)
  })

  it('sets aria-label from point label', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    const points = wrapper.findAll('[data-rig-scatter-plot-3d-point]')
    expect(points[1]!.attributes('aria-label')).toBe('A')
  })

  it('uses fallback aria-label for unlabeled points', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    const points = wrapper.findAll('[data-rig-scatter-plot-3d-point]')
    expect(points[0]!.attributes('aria-label')).toContain('1')
  })

  it('applies custom point color', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data } })
    const points = wrapper.findAll('[data-rig-scatter-plot-3d-point]')
    expect(points[1]!.attributes('fill')).toBe('#ff0000')
  })

  it('applies custom width and height', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data, width: 600, height: 600 } })
    expect(wrapper.find('svg').attributes('width')).toBe('600')
    expect(wrapper.find('svg').attributes('height')).toBe('600')
  })

  it('renders empty data with axes only', () => {
    const wrapper = mount(ScatterPlot3D, { props: { data: [] } })
    expect(wrapper.findAll('[data-rig-scatter-plot-3d-point]')).toHaveLength(0)
    expect(wrapper.findAll('[data-rig-scatter-plot-3d-axis]').length).toBeGreaterThan(0)
  })
})

