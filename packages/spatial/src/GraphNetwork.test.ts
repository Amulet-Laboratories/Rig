import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import GraphNetwork from './GraphNetwork.vue'

const nodes = [
  { id: 'a', label: 'Node A' },
  { id: 'b', label: 'Node B' },
  { id: 'c', label: 'Node C' },
]

const edges = [
  { source: 'a', target: 'b' },
  { source: 'b', target: 'c' },
]

describe('GraphNetwork', () => {
  it('renders with data-rig-graph-network', () => {
    const wrapper = mount(GraphNetwork, { props: { nodes, edges } })
    expect(wrapper.find('[data-rig-graph-network]').exists()).toBe(true)
  })

  it('renders correct number of nodes', () => {
    const wrapper = mount(GraphNetwork, { props: { nodes, edges } })
    expect(wrapper.findAll('[data-rig-graph-network-node]')).toHaveLength(3)
  })

  it('renders correct number of edges', () => {
    const wrapper = mount(GraphNetwork, { props: { nodes, edges } })
    expect(wrapper.findAll('[data-rig-graph-network-edge]')).toHaveLength(2)
  })

  it('renders node labels when showLabels is true', () => {
    const wrapper = mount(GraphNetwork, { props: { nodes, edges, showLabels: true } })
    expect(wrapper.findAll('[data-rig-graph-network-node-label]')).toHaveLength(3)
  })

  it('emits node-click on click', async () => {
    const wrapper = mount(GraphNetwork, { props: { nodes, edges } })
    await wrapper.findAll('[data-rig-graph-network-node]')[0]!.trigger('click')
    expect(wrapper.emitted('node-click')).toBeTruthy()
  })

  it('emits edge-click on click', async () => {
    const wrapper = mount(GraphNetwork, { props: { nodes, edges } })
    await wrapper.findAll('[data-rig-graph-network-edge]')[0]!.trigger('click')
    expect(wrapper.emitted('edge-click')).toBeTruthy()
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(GraphNetwork, { props: { nodes, edges } })
    expect(wrapper.find('svg').attributes('role')).toBe('group')
  })

  it('handles keyboard navigation', () => {
    const wrapper = mount(GraphNetwork, { props: { nodes, edges } })
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Network graph')
  })

  it('supports focus management', () => {
    const wrapper = mount(GraphNetwork, { props: { nodes, edges } })
    expect(wrapper.findAll('[data-rig-graph-network-node]')[0]!.attributes('tabindex')).toBe('0')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(GraphNetwork, { props: { nodes, edges } })
    expect(wrapper.findAll('[data-rig-graph-network-node]')).toHaveLength(3)
    await wrapper.setProps({ nodes: [{ id: 'a', label: 'Only A' }] })
    expect(wrapper.findAll('[data-rig-graph-network-node]')).toHaveLength(1)
  })
})

// ── Interaction test coverage ────────────────────────────────────────────────
