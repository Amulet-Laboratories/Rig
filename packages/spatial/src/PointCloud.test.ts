import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import PointCloud from './PointCloud.vue'

const data = [
  { x: 1, y: 2 },
  { x: 3, y: 4, color: '#ff0000', size: 5 },
  { x: 5, y: 1, label: 'A' },
]

describe('PointCloud', () => {
  it('renders with data-rig-point-cloud', () => {
    const wrapper = mount(PointCloud, { props: { data } })
    expect(wrapper.find('[data-rig-point-cloud]').exists()).toBe(true)
  })

  it('renders correct number of points', () => {
    const wrapper = mount(PointCloud, { props: { data } })
    expect(wrapper.findAll('[data-rig-point-cloud-point]')).toHaveLength(3)
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(PointCloud, { props: { data } })
    expect(wrapper.find('svg').attributes('role')).toBe('group')
  })

  it('emits point-click on click', async () => {
    const wrapper = mount(PointCloud, { props: { data } })
    await wrapper.findAll('[data-rig-point-cloud-point]')[0]!.trigger('click')
    expect(wrapper.emitted('point-click')).toBeTruthy()
  })

  it('applies density mode opacity', () => {
    const wrapper = mount(PointCloud, { props: { data, density: true } })
    const point = wrapper.find('[data-rig-point-cloud-point]')
    expect(point.attributes('opacity')).toBe('var(--rig-chart-density-opacity, 0.3)')
  })

  it('handles empty data', () => {
    const wrapper = mount(PointCloud, { props: { data: [] } })
    expect(wrapper.findAll('[data-rig-point-cloud-point]')).toHaveLength(0)
  })

  it('handles keyboard navigation', () => {
    const wrapper = mount(PointCloud, { props: { data } })
    // Labeled points are focusable; test data includes one point with label 'A'
    const labeledPoint = wrapper
      .findAll('[data-rig-point-cloud-point]')
      .find((p) => p.attributes('aria-label') === 'A')
    expect(labeledPoint?.attributes('tabindex')).toBe('0')
  })

  it('supports focus management', () => {
    const wrapper = mount(PointCloud, { props: { data } })
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Point cloud')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(PointCloud, { props: { data } })
    expect(wrapper.findAll('[data-rig-point-cloud-point]')).toHaveLength(3)
    await wrapper.setProps({ data: [{ x: 1, y: 2 }] })
    expect(wrapper.findAll('[data-rig-point-cloud-point]')).toHaveLength(1)
  })
})

// ── Interaction test coverage ────────────────────────────────────────────────
