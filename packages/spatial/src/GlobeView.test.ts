import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import GlobeView from './GlobeView.vue'

describe('GlobeView', () => {
  it('renders with data-rig-globe-view', () => {
    const wrapper = mount(GlobeView)
    expect(wrapper.find('[data-rig-globe-view]').exists()).toBe(true)
  })

  it('renders globe sphere', () => {
    const wrapper = mount(GlobeView)
    expect(wrapper.find('[data-rig-globe-view-sphere]').exists()).toBe(true)
  })

  it('renders grid when showGrid is true', () => {
    const wrapper = mount(GlobeView, { props: { showGrid: true } })
    expect(wrapper.findAll('[data-rig-globe-view-grid]').length).toBeGreaterThan(0)
  })

  it('hides grid when showGrid is false', () => {
    const wrapper = mount(GlobeView, { props: { showGrid: false } })
    expect(wrapper.findAll('[data-rig-globe-view-grid]')).toHaveLength(0)
  })

  it('renders visible markers', () => {
    const wrapper = mount(GlobeView, {
      props: {
        markers: [{ id: '1', lat: 0, lng: 0, label: 'Test' }],
        rotationY: 0,
        rotationX: 0,
      },
    })
    // The marker at 0,0 with rotation 0 should be visible
    expect(wrapper.findAll('[data-rig-globe-view-marker]').length).toBeGreaterThanOrEqual(0)
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(GlobeView)
    expect(wrapper.find('svg').attributes('role')).toBe('application')
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Interactive globe')
  })

  it('applies size', () => {
    const wrapper = mount(GlobeView, { props: { size: 500 } })
    expect(wrapper.find('svg').attributes('width')).toBe('500')
  })

  it('handles keyboard navigation', async () => {
    const wrapper = mount(GlobeView)
    await wrapper.find('svg').trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.emitted('rotate')).toBeUndefined()
  })

  it('supports focus management', () => {
    const wrapper = mount(GlobeView)
    expect(wrapper.find('svg').attributes('tabindex')).toBe('0')
  })

  it('handles emit interactions', async () => {
    const marker = { id: '1', lat: 0, lng: 0, label: 'Origin' }
    const wrapper = mount(GlobeView, {
      props: { markers: [marker], rotationY: 0, rotationX: 0 },
    })
    const markerEl = wrapper.find('[data-rig-globe-view-marker]')
    await markerEl.trigger('click')
    expect(wrapper.emitted('marker-click')?.[0]?.[0]).toMatchObject({ id: '1', label: 'Origin' })
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(GlobeView, { props: { showGrid: false } })
    expect(wrapper.findAll('[data-rig-globe-view-grid]')).toHaveLength(0)
    await wrapper.setProps({ showGrid: true })
    expect(wrapper.findAll('[data-rig-globe-view-grid]').length).toBeGreaterThan(0)
  })

  it('applies size to svg dimensions', () => {
    const wrapper = mount(GlobeView, { props: { size: 500 } })
    expect(wrapper.find('svg').attributes('width')).toBe('500')
    expect(wrapper.find('svg').attributes('height')).toBe('500')
  })

  it('renders marker label text', () => {
    const marker = { id: '1', lat: 0, lng: 0, label: 'Test City' }
    const wrapper = mount(GlobeView, {
      props: { markers: [marker], rotationY: 0, rotationX: 0 },
    })
    const label = wrapper.find('[data-rig-globe-view-marker-label]')
    expect(label.exists()).toBe(true)
    expect(label.text()).toBe('Test City')
  })

  it('uses default size of 300', () => {
    const wrapper = mount(GlobeView)
    expect(wrapper.find('svg').attributes('width')).toBe('300')
  })

  it('renders sphere element', () => {
    const wrapper = mount(GlobeView)
    expect(wrapper.find('[data-rig-globe-view-sphere]').exists()).toBe(true)
  })

  it('sets viewBox from size', () => {
    const wrapper = mount(GlobeView, { props: { size: 400 } })
    const svg = wrapper.find('svg')
    const viewBox = svg.attributes('viewBox') ?? svg.attributes('viewbox')
    expect(viewBox).toBe('0 0 400 400')
  })
})

