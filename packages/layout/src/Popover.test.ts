describe('Popover interactions', () => {
  afterEach(() => {
    document.querySelectorAll('[data-rig-popover-content]').forEach((el) => el.remove())
  })

  it('supports focus management', async () => {
    const wrapper = mount(Popover, {
      props: { open: true },
      attachTo: document.body,
    })
    const focusable = wrapper.find('[tabindex], input, button, [role], a')
    if (focusable.exists()) {
      ;(focusable.element as HTMLElement).focus()
      expect(document.activeElement).toBe(focusable.element)
    } else {
      // Non-interactive component — verify it renders without needing focus
      expect(wrapper.html()).toBeTruthy()
    }
    wrapper.unmount()
  })

  it('sets inert and aria-hidden when closed', () => {
    const wrapper = factory({ open: false })
    const content = document.querySelector('[data-rig-popover-content]') as HTMLElement
    expect(content.getAttribute('aria-hidden')).toBe('true')
    expect(content.hasAttribute('inert')).toBe(true)
    wrapper.unmount()
  })

  it('removes inert and aria-hidden when open', () => {
    const wrapper = factory({ open: true })
    const content = document.querySelector('[data-rig-popover-content]') as HTMLElement
    expect(content.hasAttribute('aria-hidden')).toBe(false)
    expect(content.hasAttribute('inert')).toBe(false)
    wrapper.unmount()
  })

  it('closes on click outside when not persistent', async () => {
    const wrapper = factory({ open: false })
    await wrapper.setProps({ open: true })
    // Create an external element to click on
    const outside = document.createElement('div')
    document.body.appendChild(outside)
    outside.click()
    await new Promise((r) => setTimeout(r, 0))
    expect(wrapper.emitted('update:open')?.[0]).toEqual([false])
    outside.remove()
    wrapper.unmount()
  })

  it('does not close on click outside when persistent', async () => {
    const wrapper = factory({ open: false, persistent: true })
    await wrapper.setProps({ open: true })
    const outside = document.createElement('div')
    document.body.appendChild(outside)
    outside.click()
    await new Promise((r) => setTimeout(r, 0))
    expect(wrapper.emitted('update:open')).toBeUndefined()
    outside.remove()
    wrapper.unmount()
  })

  it('reflects data-state on content element', async () => {
    const wrapper = factory({ open: false })
    await wrapper.setProps({ open: true })
    const content = document.querySelector('[data-rig-popover-content]') as HTMLElement
    expect(content.getAttribute('data-state')).toBe('open')
    await wrapper.setProps({ open: false })
    expect(content.getAttribute('data-state')).toBe('closed')
    wrapper.unmount()
  })
})
