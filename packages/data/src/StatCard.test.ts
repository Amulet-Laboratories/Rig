import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import StatCard from './StatCard.vue'

describe('StatCard', () => {
  it('renders with data-rig-stat-card', () => {
    const wrapper = mount(StatCard, { props: { label: 'Revenue', value: '$10k' } })
    expect(wrapper.find('[data-rig-stat-card]').exists()).toBe(true)
  })

  it('displays label and value', () => {
    const wrapper = mount(StatCard, { props: { label: 'Users', value: '1,234' } })
    expect(wrapper.find('[data-rig-stat-card-label]').text()).toBe('Users')
    expect(wrapper.find('[data-rig-stat-card-value]').text()).toBe('1,234')
  })

  it('displays trend', () => {
    const wrapper = mount(StatCard, {
      props: { label: 'Sales', value: '500', trend: 'up', trendValue: '+12%' },
    })
    const trend = wrapper.find('[data-rig-stat-card-trend]')
    expect(trend.text()).toBe('+12%')
    expect(trend.attributes('data-trend')).toBe('up')
  })

  it('displays description', () => {
    const wrapper = mount(StatCard, {
      props: { label: 'Test', value: '0', description: 'vs last week' },
    })
    expect(wrapper.find('[data-rig-stat-card-description]').text()).toBe('vs last week')
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(StatCard, { props: { label: 'Revenue', value: '$10k' } })
    expect(wrapper.find('[data-rig-stat-card]').attributes('role')).toBe('group')
    expect(wrapper.find('[data-rig-stat-card]').attributes('aria-label')).toBe('Revenue')
  })

  it('renders slot content', () => {
    const wrapper = mount(StatCard, {
      props: { label: 'Test', value: '0' },
      slots: { default: '<div class="custom">Extra</div>' },
    })
    expect(wrapper.find('.custom').text()).toBe('Extra')
  })

  it('does not emit custom events on keydown', async () => {
    const wrapper = mount(StatCard, { props: { label: 'Test', value: 42 } })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.find('[data-rig-stat-card]').attributes('role')).toBe('group')
  })

  it('supports focus management', () => {
    const wrapper = mount(StatCard, { props: { label: 'Test', value: 42 } })
    expect(wrapper.find('[data-rig-stat-card]').attributes('tabindex')).toBe('0')
  })

  it('does not define custom emits', async () => {
    const wrapper = mount(StatCard, { props: { label: 'Test', value: 42 } })
    await wrapper.trigger('click')
    expect(wrapper.find('[data-rig-stat-card]').attributes('aria-label')).toBe('Test')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(StatCard, { props: { label: 'Test', value: 42 } })
    expect(wrapper.find('[data-rig-stat-card-value]').text()).toBe('42')
    await wrapper.setProps({ value: 75 })
    expect(wrapper.find('[data-rig-stat-card-value]').text()).toBe('75')
  })
})

