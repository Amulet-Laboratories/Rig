import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import LineChart from './LineChart.vue'

const series = [
  { label: 'Revenue', data: [10, 20, 15, 30] },
  { label: 'Costs', data: [5, 8, 12, 10], color: '#ff0000' },
]

describe('LineChart', () => {
  it('renders with data-rig-line-chart', () => {
    const wrapper = mount(LineChart, { props: { series } })
    expect(wrapper.find('[data-rig-line-chart]').exists()).toBe(true)
  })

  it('renders correct number of series', () => {
    const wrapper = mount(LineChart, { props: { series } })
    expect(wrapper.findAll('[data-rig-line-chart-series]')).toHaveLength(2)
  })

  it('renders line paths', () => {
    const wrapper = mount(LineChart, { props: { series } })
    expect(wrapper.findAll('[data-rig-line-chart-line]')).toHaveLength(2)
  })

  it('shows dots when enabled', () => {
    const wrapper = mount(LineChart, { props: { series, showDots: true } })
    expect(wrapper.findAll('[data-rig-line-chart-dot]').length).toBeGreaterThan(0)
  })

  it('hides dots by default', () => {
    const wrapper = mount(LineChart, { props: { series } })
    expect(wrapper.findAll('[data-rig-line-chart-dot]')).toHaveLength(0)
  })

  it('renders labels', () => {
    const wrapper = mount(LineChart, { props: { series, labels: ['Q1', 'Q2', 'Q3', 'Q4'] } })
    expect(wrapper.findAll('[data-rig-line-chart-label]')).toHaveLength(4)
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(LineChart, { props: { series } })
    expect(wrapper.find('svg').attributes('role')).toBe('img')
  })

  it('does not emit point-click on keydown', async () => {
    const wrapper = mount(LineChart, {
      props: { series: [{ label: 's', data: [1, 2], color: 'red' }] },
    })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.emitted('point-click')).toBeUndefined()
  })

  it('supports focus management', () => {
    const wrapper = mount(LineChart, {
      props: { series: [{ label: 's', data: [1, 2], color: 'red' }] },
    })
    expect(wrapper.find('svg').attributes('tabindex')).toBe('0')
  })

  it('emits point-click when dot is clicked', async () => {
    const wrapper = mount(LineChart, {
      props: { series: [{ label: 's', data: [1, 2], color: 'red' }], showDots: true },
    })
    await wrapper.findAll('[data-rig-line-chart-dot]')[0]!.trigger('click')
    expect(wrapper.emitted('point-click')).toHaveLength(1)
    expect(wrapper.emitted('point-click')![0]![1]).toBe(0)
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(LineChart, {
      props: { series: [{ label: 's', data: [1, 2], color: 'red' }] },
    })
    expect(wrapper.findAll('[data-rig-line-chart-series]')).toHaveLength(1)
    await wrapper.setProps({
      series: [
        { label: 'a', data: [1, 2] },
        { label: 'b', data: [3, 4] },
      ],
    })
    expect(wrapper.findAll('[data-rig-line-chart-series]')).toHaveLength(2)
  })
})

