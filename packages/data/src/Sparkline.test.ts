import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import Sparkline from './Sparkline.vue'

describe('Sparkline', () => {
  it('renders with data-rig-sparkline', () => {
    const wrapper = mount(Sparkline, { props: { data: [1, 2, 3] } })
    expect(wrapper.find('[data-rig-sparkline]').exists()).toBe(true)
  })

  it('renders SVG with correct dimensions', () => {
    const wrapper = mount(Sparkline, { props: { data: [1, 2, 3], width: 200, height: 40 } })
    const svg = wrapper.find('svg')
    expect(svg.attributes('width')).toBe('200')
    expect(svg.attributes('height')).toBe('40')
  })

  it('renders line path when data provided', () => {
    const wrapper = mount(Sparkline, { props: { data: [1, 3, 2, 4] } })
    expect(wrapper.find('[data-rig-sparkline-line]').exists()).toBe(true)
  })

  it('does not render area by default', () => {
    const wrapper = mount(Sparkline, { props: { data: [1, 2, 3] } })
    expect(wrapper.find('[data-rig-sparkline-area]').exists()).toBe(false)
  })

  it('renders area fill when fill=true', () => {
    const wrapper = mount(Sparkline, { props: { data: [1, 2, 3], fill: true } })
    expect(wrapper.find('[data-rig-sparkline-area]').exists()).toBe(true)
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(Sparkline, { props: { data: [1, 2] } })
    expect(wrapper.find('svg').attributes('role')).toBe('img')
  })

  it('handles empty data', () => {
    const wrapper = mount(Sparkline, { props: { data: [] } })
    expect(wrapper.find('[data-rig-sparkline-line]').exists()).toBe(false)
  })

  it('does not emit custom events on keydown', async () => {
    const wrapper = mount(Sparkline, { props: { data: [1, 2, 3] } })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Sparkline chart')
  })

  it('supports focus management', () => {
    const wrapper = mount(Sparkline, { props: { data: [1, 2, 3] } })
    expect(wrapper.find('svg').attributes('tabindex')).toBe('0')
  })

  it('does not define custom emits', async () => {
    const wrapper = mount(Sparkline, { props: { data: [1, 2, 3] } })
    await wrapper.trigger('click')
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Sparkline chart')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(Sparkline, { props: { data: [1, 2, 3] } })
    expect(wrapper.find('[data-rig-sparkline-line]').exists()).toBe(true)
    await wrapper.setProps({ data: [] })
    expect(wrapper.find('[data-rig-sparkline-line]').exists()).toBe(false)
  })
})

