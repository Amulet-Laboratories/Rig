import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import RadarChart from './RadarChart.vue'

const axes = [
  { label: 'Speed', max: 100 },
  { label: 'Power', max: 100 },
  { label: 'Range', max: 100 },
  { label: 'Defense', max: 100 },
  { label: 'Magic', max: 100 },
]

const series = [{ label: 'Hero', data: [80, 60, 90, 40, 70] }]

describe('RadarChart', () => {
  it('renders with data-rig-radar-chart', () => {
    const wrapper = mount(RadarChart, { props: { axes, series } })
    expect(wrapper.find('[data-rig-radar-chart]').exists()).toBe(true)
  })

  it('renders ring paths', () => {
    const wrapper = mount(RadarChart, { props: { axes, series, rings: 3 } })
    expect(wrapper.findAll('[data-rig-radar-chart-ring]')).toHaveLength(3)
  })

  it('renders axis lines', () => {
    const wrapper = mount(RadarChart, { props: { axes, series } })
    expect(wrapper.findAll('[data-rig-radar-chart-axis]')).toHaveLength(5)
  })

  it('renders series', () => {
    const wrapper = mount(RadarChart, { props: { axes, series } })
    expect(wrapper.findAll('[data-rig-radar-chart-series]')).toHaveLength(1)
  })

  it('renders axis labels', () => {
    const wrapper = mount(RadarChart, { props: { axes, series } })
    const labels = wrapper.findAll('[data-rig-radar-chart-label]')
    expect(labels).toHaveLength(5)
    expect(labels[0]!.text()).toBe('Speed')
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(RadarChart, { props: { axes, series } })
    expect(wrapper.find('svg').attributes('role')).toBe('img')
  })

  it('does not emit custom events on keydown', async () => {
    const wrapper = mount(RadarChart, {
      props: { axes: [{ label: 'A' }], series: [{ label: 's', data: [1] }] },
    })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Radar chart')
  })

  it('supports focus management', () => {
    const wrapper = mount(RadarChart, {
      props: { axes: [{ label: 'A' }], series: [{ label: 's', data: [1] }] },
    })
    expect(wrapper.find('svg').attributes('tabindex')).toBe('0')
  })

  it('does not define custom emits', async () => {
    const wrapper = mount(RadarChart, {
      props: { axes: [{ label: 'A' }], series: [{ label: 's', data: [1] }] },
    })
    await wrapper.trigger('click')
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Radar chart')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(RadarChart, {
      props: { axes: [{ label: 'A' }], series: [{ label: 's', data: [1] }] },
    })
    expect(wrapper.findAll('[data-rig-radar-chart-series]')).toHaveLength(1)
    await wrapper.setProps({
      series: [
        { label: 'a', data: [1] },
        { label: 'b', data: [2] },
      ],
    })
    expect(wrapper.findAll('[data-rig-radar-chart-series]')).toHaveLength(2)
  })
})

