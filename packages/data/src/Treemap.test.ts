import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import Treemap from './Treemap.vue'

const data = [
  { label: 'A', value: 30 },
  { label: 'B', value: 20 },
  { label: 'C', value: 50 },
]

describe('Treemap', () => {
  it('renders with data-rig-treemap', () => {
    const wrapper = mount(Treemap, { props: { data } })
    expect(wrapper.find('[data-rig-treemap]').exists()).toBe(true)
  })

  it('renders correct number of nodes', () => {
    const wrapper = mount(Treemap, { props: { data } })
    expect(wrapper.findAll('[data-rig-treemap-node]')).toHaveLength(3)
  })

  it('renders rects', () => {
    const wrapper = mount(Treemap, { props: { data } })
    expect(wrapper.findAll('[data-rig-treemap-rect]')).toHaveLength(3)
  })

  it('emits node-click on click', async () => {
    const wrapper = mount(Treemap, { props: { data } })
    await wrapper.findAll('[data-rig-treemap-node]')[0]!.trigger('click')
    const emitted = wrapper.emitted('node-click')!
    expect(emitted[0]![0]).toEqual({ label: 'A', value: 30 })
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(Treemap, { props: { data } })
    expect(wrapper.find('svg').attributes('role')).toBe('img')
  })

  it('handles empty data', () => {
    const wrapper = mount(Treemap, { props: { data: [] } })
    expect(wrapper.findAll('[data-rig-treemap-node]')).toHaveLength(0)
  })

  it('does not emit node-click on keydown', async () => {
    const wrapper = mount(Treemap, { props: { data: [{ label: 'A', value: 10 }] } })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.emitted('node-click')).toBeUndefined()
  })

  it('supports focus management', () => {
    const wrapper = mount(Treemap, { props: { data: [{ label: 'A', value: 10 }] } })
    expect(wrapper.find('svg').attributes('tabindex')).toBe('0')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(Treemap, { props: { data: [{ label: 'A', value: 10 }] } })
    expect(wrapper.findAll('[data-rig-treemap-node]')).toHaveLength(1)
    await wrapper.setProps({
      data: [
        { label: 'A', value: 10 },
        { label: 'B', value: 20 },
      ],
    })
    expect(wrapper.findAll('[data-rig-treemap-node]')).toHaveLength(2)
  })
})

