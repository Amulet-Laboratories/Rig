import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import BarChart from './BarChart.vue'

const data = [
  { label: 'A', value: 10 },
  { label: 'B', value: 20 },
  { label: 'C', value: 15 },
]

describe('BarChart', () => {
  it('renders with data-rig-bar-chart', () => {
    const wrapper = mount(BarChart, { props: { data } })
    expect(wrapper.find('[data-rig-bar-chart]').exists()).toBe(true)
  })

  it('renders correct number of bars', () => {
    const wrapper = mount(BarChart, { props: { data } })
    expect(wrapper.findAll('[data-rig-bar-chart-bar]')).toHaveLength(3)
  })

  it('renders labels when showLabels is true', () => {
    const wrapper = mount(BarChart, { props: { data, showLabels: true } })
    const labels = wrapper.findAll('[data-rig-bar-chart-label]')
    expect(labels).toHaveLength(3)
    expect(labels[0]!.text()).toBe('A')
  })

  it('renders values when showValues is true', () => {
    const wrapper = mount(BarChart, { props: { data, showValues: true } })
    expect(wrapper.findAll('[data-rig-bar-chart-value]')).toHaveLength(3)
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(BarChart, { props: { data } })
    expect(wrapper.find('svg').attributes('role')).toBe('img')
  })

  it('emits bar-click on click', async () => {
    const wrapper = mount(BarChart, { props: { data } })
    await wrapper.findAll('[data-rig-bar-chart-bar-group]')[0]!.trigger('click')
    expect(wrapper.emitted('bar-click')).toBeTruthy()
  })

  it('supports horizontal orientation', () => {
    const wrapper = mount(BarChart, { props: { data, orientation: 'horizontal' } })
    expect(wrapper.findAll('[data-rig-bar-chart-bar]')).toHaveLength(3)
  })

  it('does not emit bar-click on keydown', async () => {
    const wrapper = mount(BarChart, { props: { data: [{ label: 'A', value: 10 }] } })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.emitted('bar-click')).toBeUndefined()
  })

  it('supports focus management', () => {
    const wrapper = mount(BarChart, { props: { data: [{ label: 'A', value: 10 }] } })
    expect(wrapper.find('svg').attributes('tabindex')).toBe('0')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(BarChart, { props: { data: [{ label: 'A', value: 10 }] } })
    expect(wrapper.findAll('[data-rig-bar-chart-bar]')).toHaveLength(1)
    await wrapper.setProps({
      data: [
        { label: 'A', value: 10 },
        { label: 'B', value: 20 },
      ],
    })
    expect(wrapper.findAll('[data-rig-bar-chart-bar]')).toHaveLength(2)
  })

  it('hides labels when showLabels is false', () => {
    const wrapper = mount(BarChart, { props: { data, showLabels: false } })
    expect(wrapper.findAll('[data-rig-bar-chart-label]')).toHaveLength(0)
  })

  it('hides labels in horizontal orientation', () => {
    const wrapper = mount(BarChart, {
      props: { data, orientation: 'horizontal', showLabels: true },
    })
    expect(wrapper.findAll('[data-rig-bar-chart-label]')).toHaveLength(0)
  })

  it('applies custom width and height', () => {
    const wrapper = mount(BarChart, { props: { data, width: 800, height: 600 } })
    expect(wrapper.find('svg').attributes('width')).toBe('800')
    expect(wrapper.find('svg').attributes('height')).toBe('600')
  })

  it('sets aria-label on each bar', () => {
    const wrapper = mount(BarChart, { props: { data: [{ label: 'Sales', value: 42 }] } })
    const bar = wrapper.find('[data-rig-bar-chart-bar]')
    expect(bar.attributes('aria-label')).toBe('Sales: 42')
  })

  it('applies custom bar color', () => {
    const wrapper = mount(BarChart, {
      props: { data: [{ label: 'A', value: 10, color: '#ff0000' }] },
    })
    expect(wrapper.find('[data-rig-bar-chart-bar]').attributes('style')).toContain('fill: #ff0000')
  })

  it('renders empty data gracefully', () => {
    const wrapper = mount(BarChart, { props: { data: [] } })
    expect(wrapper.findAll('[data-rig-bar-chart-bar]')).toHaveLength(0)
    expect(wrapper.find('svg').exists()).toBe(true)
  })
})

