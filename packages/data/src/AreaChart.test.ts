import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import AreaChart from './AreaChart.vue'

const series = [{ label: 'Sales', data: [10, 20, 15, 30, 25] }]

describe('AreaChart', () => {
  it('renders with data-rig-area-chart', () => {
    const wrapper = mount(AreaChart, { props: { series } })
    expect(wrapper.find('[data-rig-area-chart]').exists()).toBe(true)
  })

  it('renders fill and line paths', () => {
    const wrapper = mount(AreaChart, { props: { series } })
    expect(wrapper.find('[data-rig-area-chart-fill]').exists()).toBe(true)
    expect(wrapper.find('[data-rig-area-chart-line]').exists()).toBe(true)
  })

  it('renders correct number of series', () => {
    const wrapper = mount(AreaChart, {
      props: { series: [...series, { label: 'Costs', data: [5, 10, 8, 15, 12] }] },
    })
    expect(wrapper.findAll('[data-rig-area-chart-series]')).toHaveLength(2)
  })

  it('renders labels', () => {
    const wrapper = mount(AreaChart, {
      props: { series, labels: ['Jan', 'Feb', 'Mar', 'Apr', 'May'] },
    })
    expect(wrapper.findAll('[data-rig-area-chart-label]')).toHaveLength(5)
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(AreaChart, { props: { series } })
    expect(wrapper.find('svg').attributes('role')).toBe('img')
  })

  it('does not emit custom events on keydown', async () => {
    const wrapper = mount(AreaChart, {
      props: { series: [{ label: 's', data: [1, 2], color: 'red' }] },
    })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Area chart')
  })

  it('supports focus management', () => {
    const wrapper = mount(AreaChart, {
      props: { series: [{ label: 's', data: [1, 2], color: 'red' }] },
    })
    expect(wrapper.find('svg').attributes('tabindex')).toBe('0')
  })

  it('does not define custom emits', async () => {
    const wrapper = mount(AreaChart, {
      props: { series: [{ label: 's', data: [1, 2], color: 'red' }] },
    })
    await wrapper.trigger('click')
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Area chart')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(AreaChart, {
      props: { series: [{ label: 's', data: [1, 2], color: 'red' }] },
    })
    expect(wrapper.findAll('[data-rig-area-chart-series]')).toHaveLength(1)
    await wrapper.setProps({
      series: [
        { label: 'a', data: [1, 2] },
        { label: 'b', data: [3, 4] },
      ],
    })
    expect(wrapper.findAll('[data-rig-area-chart-series]')).toHaveLength(2)
  })
})

