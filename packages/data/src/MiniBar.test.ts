import { describe, it, expect } from 'vitest'
import { mount } from '@vue/test-utils'
import MiniBar from './MiniBar.vue'

describe('MiniBar', () => {
  it('renders with data-rig-mini-bar', () => {
    const wrapper = mount(MiniBar, { props: { data: [1, 2, 3] } })
    expect(wrapper.find('[data-rig-mini-bar]').exists()).toBe(true)
  })

  it('renders correct number of bars', () => {
    const wrapper = mount(MiniBar, { props: { data: [1, 2, 3, 4] } })
    expect(wrapper.findAll('[data-rig-mini-bar-rect]')).toHaveLength(4)
  })

  it('renders SVG with correct dimensions', () => {
    const wrapper = mount(MiniBar, { props: { data: [1, 2], width: 100, height: 24 } })
    const svg = wrapper.find('svg')
    expect(svg.attributes('width')).toBe('100')
    expect(svg.attributes('height')).toBe('24')
  })

  it('has correct ARIA attributes', () => {
    const wrapper = mount(MiniBar, { props: { data: [1, 2] } })
    expect(wrapper.find('svg').attributes('role')).toBe('img')
  })

  it('handles empty data', () => {
    const wrapper = mount(MiniBar, { props: { data: [] } })
    expect(wrapper.findAll('[data-rig-mini-bar-rect]')).toHaveLength(0)
  })

  it('does not emit custom events on keydown', async () => {
    const wrapper = mount(MiniBar, { props: { data: [5, 10] } })
    await wrapper.trigger('keydown', { key: 'ArrowDown' })
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Bar chart')
  })

  it('supports focus management', () => {
    const wrapper = mount(MiniBar, { props: { data: [5, 10] } })
    expect(wrapper.find('svg').attributes('tabindex')).toBe('0')
  })

  it('does not define custom emits', async () => {
    const wrapper = mount(MiniBar, { props: { data: [5, 10] } })
    await wrapper.trigger('click')
    expect(wrapper.find('svg').attributes('aria-label')).toBe('Bar chart')
  })

  it('reacts to prop updates', async () => {
    const wrapper = mount(MiniBar, { props: { data: [5, 10] } })
    expect(wrapper.findAll('[data-rig-mini-bar-rect]')).toHaveLength(2)
    await wrapper.setProps({ data: [1, 2, 3] })
    expect(wrapper.findAll('[data-rig-mini-bar-rect]')).toHaveLength(3)
  })
})

